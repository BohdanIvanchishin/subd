SELECT * FROM пацієнт
WHERE Стать = 'Чоловік'
ORDER BY Прізвище DESC;

SELECT Номер_реєстрації, Код_пацієнта, Код_лікаря, Дата_запису
FROM реєстрація
ORDER BY Дата_запису DESC LIMIT 5;

SELECT Назва, COUNT(реєстрація.Код_пацієнта) as 'Кількість пацієнтів' 
FROM відділення
INNER JOIN лікар ON лікар.Код_відділення = відділення.Код_відділення
INNER JOIN реєстрація ON реєстрація.Код_лікаря = лікар.Код_лікаря
GROUP BY Назва
